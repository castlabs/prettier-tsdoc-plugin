/**
 * HTTP status code enumeration
 *
 * @enum
 * @internal
 */
export const HttpStatus = {
  /**
   * Success response
   */
  OK: 200,

  /**
   * Resource not found
   */
  NotFound: 404,

  /**
   * Internal server error
   */
  InternalError: 500,

  /**
   * Reserved for internal use only
   */
  Reserved: 999,
};
